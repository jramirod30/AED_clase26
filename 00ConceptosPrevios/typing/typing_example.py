# dynamic typing
data = 35
print(f" type of data is: {type(data)}")
data = "35"
print(f" type of data is: {type(data)}")
# type hints
data2: int = 20
print(f" type of data is: {type(data2)}")
data2 = "20"
print(f" type of data is: {type(data2)}")
data_bool: bool = False
print(f" type of data is: {type(data_bool)}")
data_bool = 12
print(f" type of data is: {type(data_bool)}")
